#include <stdio.h>
#include <windows.h>

// Функція для обчислення кількості чисел із заданою довжиною р
long long countNumbers(int r) {
    if (r == 1) return 2; // "5" і "9"
    if (r == 2) return 4; // "55", "59", "95", "99"

    // dp[i][0] - кількість чисел довжиною i, які закінчуються на одну цифру
    // dp[i][1] - кількість чисел довжиною i, які закінчуються на дві однакові цифри
    long long dp[r+1][2];

    dp[1][0] = 2; // "5", "9"
    dp[1][1] = 0;
    dp[2][0] = 2; // "59", "95"
    dp[2][1] = 2; // "55", "99"

    for (int i = 3; i <= r; i++) {
        dp[i][0] = dp[i-1][0] + dp[i-1][1]; // додавання "5" або "9" до числа без двох однакових цифр в кінці
        dp[i][1] = dp[i-1][0]; // додавання другої такої ж цифри до числа без однакових цифр в кінці
    }

    return dp[r][0] + dp[r][1];
}

int main() {
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    int r;
    printf("Введіть кількість розрядів р: ");
    scanf("%d", &r);

    if (r <= 0 || r > 30) {
        printf("Кількість розрядів повинна бути від 1 до 30.\n");
        return 1;
    }

    long long result = countNumbers(r);
    printf("Кількість чисел із %d розрядів: %lld\n", r, result);

    return 0;
}
